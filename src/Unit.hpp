#pragma once

////////////////////////////////////////////////////////////
// Headers
////////////////////////////////////////////////////////////

#include "Unit.hpp"
#include "Actor.hpp"

////////////////////////////////////////////////////////////
///
/// \brief The Unit class defines an Actor with the ability to be moved
///
////////////////////////////////////////////////////////////

class Unit : public Actor
{

public:

	Unit();
	~Unit(void);

	////////////////////////////////////////////////////////////
	///
	/// \brief Defines the position to Move To
	///
	////////////////////////////////////////////////////////////
	void moveTo( float x, float y );

	////////////////////////////////////////////////////////////
	///
	/// \brief Set the Units Position
	///
	////////////////////////////////////////////////////////////
	inline void setPosition( float x, float y ) { m_position.x = x; m_position.y = y; } 
	
	////////////////////////////////////////////////////////////
	///
	/// \brief Gets the Unit position
	/// \return ActorPosition
	///
	////////////////////////////////////////////////////////////
	inline ActorPosition getPosition() { return m_position; }
};