#include <SFML/Graphics.hpp>

#include "Unit.hpp"
#include "Command.hpp"

////////////////////////////////////////////////////////////
/// \brief The MoveUnitCommand is derived from Command
/// This derived Command is used to tell a Unit to Move
////////////////////////////////////////////////////////////

class MoveUnitCommand : public Command
{
public:

	MoveUnitCommand( Unit* unit, float x, float y );
	~MoveUnitCommand(void);

	////////////////////////////////////////////////////////////
	/// \brief This method is Inherited from Command, and in this case is used to move the Unit
	////////////////////////////////////////////////////////////
	void execute();

private:
	////////////////////////////////////////////////////////////
	/// \brief A Handle to a unit / list of units
	////////////////////////////////////////////////////////////
	Unit*         m_unit;

	////////////////////////////////////////////////////////////
	/// \brief The Position the unit is to move to
	////////////////////////////////////////////////////////////
	ActorPosition m_position;
};